#include "libft.h"
#include <stdlib.h>

void	ft_lstdelone(t_list **alst, void (*del)(void *, size_t))
{
	if (alst != NULL)
	{
		del((*alst)->content, sizeof((*alst)->content_size));
		free(*alst);
		*alst = NULL;
	}
}
